    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        try{
            String SDO_Drogas =txtBuscarDR.getText().toString();

            DefaultTableModel tabla= new DefaultTableModel();

            tabla.addColumn("Codigo");
            tabla.addColumn("Descripcion");
            tabla.addColumn("Unidad de Medida");
            tabla.addColumn("Stock");
            cst=con.prepareCall("{call SALA_OPERACION_DROGAS_BUSCAR(?)}");
            cst.setString(1, SDO_Drogas);
            r=cst.executeQuery();
            while (r.next()){
                Object dato[]=new  Object[4];
                for (int i=0; i<4; i++){
                    dato[i]=r.getString(i+1);

                }
                tabla.addRow(dato);
            }

            this.tb_SDO_Drogas.setModel(tabla);

            formato();
            txtBuscarDR.setText("");
            tb_SDO_Drogas.getSelectionModel().setSelectionInterval(0, 0);
            tb_SDO_Drogas.requestFocus();
        }catch (Exception e){}

    }//GEN-LAST:event_btn_buscarActionPerformed

    private void tb_SDO_DrogasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_SDO_DrogasKeyPressed

        char teclaPresionada = evt.getKeyChar();

        if(teclaPresionada==KeyEvent.VK_ENTER &&
            this.tb_SDO_Drogas.getRowCount() == 0 &&
            this.tb_SDO_Drogas.getSelectedRow() == -1){

            JOptionPane.showMessageDialog(rootPane, "La tabla esta vacia");

        }else
        if(teclaPresionada==KeyEvent.VK_ENTER &&
            this.tb_SDO_Drogas.getRowCount() != 0 &&
            this.tb_SDO_Drogas.getSelectedRow() != -1){
            int fila = tb_SDO_Drogas.getSelectedRow();
            //frm_SALA_OPERACION_TIPO_ANESTESIA T = new frm_SALA_OPERACION_TIPO_ANESTESIA();
            //T.setVisible(true);
            dispose();

            frm_SALA_OPERACION_DROGAS.txtCodigo.setText(String.valueOf(tb_SDO_Drogas.getValueAt(fila, 0)));
            frm_SALA_OPERACION_DROGAS.txtDescripcion.setText(String.valueOf(tb_SDO_Drogas.getValueAt(fila, 1)));
            frm_SALA_OPERACION_DROGAS.txtUnidad.setText(String.valueOf(tb_SDO_Drogas.getValueAt(fila, 2)));
            frm_SALA_OPERACION_DROGAS.txtStock.setText(String.valueOf(tb_SDO_Drogas.getValueAt(fila, 3)));
            frm_SALA_OPERACION_DROGAS.btnmodificar.setEnabled(true);
            frm_SALA_OPERACION_DROGAS.btneliminar.setEnabled(true);
            String u=frm_SALA_OPERACION_DROGAS.lblUsu.getText();
            frm_SALA_OPERACION_DROGAS.lblUsu.setText(u);
            frm_SALA_OPERACION_DROGAS.txtDescripcion.setEnabled(false);
            frm_SALA_OPERACION_DROGAS.txtUnidad.setEnabled(false);
            frm_SALA_OPERACION_DROGAS.txtStock.setEnabled(false);
        }
    }//GEN-LAST:event_tb_SDO_DrogasKeyPressed

