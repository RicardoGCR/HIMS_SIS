    private void tbPersonalRolKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbPersonalRolKeyPressed
        // TODO add your handling code here:
        char tecla= evt.getKeyChar();
        if(tecla==KeyEvent.VK_ENTER){
            try{
                if( tbPersonalRol.getRowCount()>0){
                    if(lbltipo.getText().equalsIgnoreCase("1")){
                        frm_LAB_ENTREGA_RESULTADO.btnCerrarDialog.doClick();
                        
                        int filaselec=tbPersonalRol.getSelectedRow();
                        String nombreCompleto=tbPersonalRol.getValueAt(filaselec, 2).toString()+" "+
                        tbPersonalRol.getValueAt(filaselec, 3).toString()
                        +" "+tbPersonalRol.getValueAt(filaselec, 4).toString();
                        frm_LAB_ENTREGA_RESULTADO.txtPersonalEntreg.setText(nombreCompleto);
                        frm_LAB_ENTREGA_RESULTADO.lblCodPerEntrega.setText(tbPersonalRol.getValueAt(filaselec, 1).toString());
                    }
                    else if(lbltipo.getText().equalsIgnoreCase("2")){
                        frm_LAB_ENTREGA_RESULTADO.btnCerrarDialog.doClick();
                        int filaselec=tbPersonalRol.getSelectedRow();
                        String nombreCompleto=tbPersonalRol.getValueAt(filaselec, 2).toString()+" "+
                        tbPersonalRol.getValueAt(filaselec, 3).toString()
                        +" "+tbPersonalRol.getValueAt(filaselec, 4).toString();
                        frm_LAB_ENTREGA_RESULTADO.txtPersonalRecep.setText(nombreCompleto);
                        frm_LAB_ENTREGA_RESULTADO.lblcodPerRecep.setText(tbPersonalRol.getValueAt(filaselec, 1).toString());

                        frm_LAB_ENTREGA_RESULTADO.lblar_idRecep.setText(tbPersonalRol.getValueAt(filaselec, 8).toString());
                        frm_LAB_ENTREGA_RESULTADO.txtAreaRecep.setText(tbPersonalRol.getValueAt(filaselec, 7).toString());
                        frm_LAB_ENTREGA_RESULTADO.txtServRecep.setText(tbPersonalRol.getValueAt(filaselec, 6).toString());
                    }
                }}
                catch(Exception ex)
                {
                    System.out.println("Error: " + ex.getMessage());
                }
            }
    }//GEN-LAST:event_tbPersonalRolKeyPressed

