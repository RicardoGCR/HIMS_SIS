            m=new DefaultTableModel(null,titulosb);
            JTable psb=new JTable(m);
            String filasb[]=new String[7];
            TB_UO_ACTIVIDADES.setModel(m);
            TableRowSorter<TableModel> elQueOrdenasb=new TableRowSorter<TableModel>(m);
