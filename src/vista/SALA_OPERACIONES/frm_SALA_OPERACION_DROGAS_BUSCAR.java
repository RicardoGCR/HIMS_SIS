    public void cargar_SDO_Drogas(){
       
    try{
       DefaultTableModel tabla= new DefaultTableModel();
      
       tabla.addColumn("Codigo");
       tabla.addColumn("Descripción");
       tabla.addColumn("Unidad de Medida");
       tabla.addColumn("Stock");
 
       
       cst=con.prepareCall("{call SALA_OPERACION_DROGAS_LISTAR}");
       r=cst.executeQuery();
       while (r.next()){
       Object dato[]=new  Object[4];
       for (int i=0; i<4; i++){
           dato[i]=r.getString(i+1);
       }
       tabla.addRow(dato);
       }
       this.tb_SDO_Drogas.setModel(tabla);
       formato();
       
       }catch (Exception e){
       }
     }
    
