    private void tb_Tipo_AnestesiaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_Tipo_AnestesiaKeyPressed
               
        char teclaPresionada = evt.getKeyChar();
       
       if(teclaPresionada==KeyEvent.VK_ENTER &&
               this.tb_Tipo_Anestesia.getRowCount() == 0 && 
               this.tb_Tipo_Anestesia.getSelectedRow() == -1){
                      
           JOptionPane.showMessageDialog(rootPane, "La tabla esta vacia");
           
       }else 
       if(teclaPresionada==KeyEvent.VK_ENTER &&
               this.tb_Tipo_Anestesia.getRowCount() != 0 && 
               this.tb_Tipo_Anestesia.getSelectedRow() != -1){
              int fila = tb_Tipo_Anestesia.getSelectedRow();
              //frm_SALA_OPERACION_TIPO_ANESTESIA T = new frm_SALA_OPERACION_TIPO_ANESTESIA();
                //T.setVisible(true);
                dispose();

             frm_SALA_OPERACION_TIPO_ANESTESIA.txtCodigo.setText(String.valueOf(tb_Tipo_Anestesia.getValueAt(fila, 0)));
             frm_SALA_OPERACION_TIPO_ANESTESIA.txtTipo_Anestesia.setText(String.valueOf(tb_Tipo_Anestesia.getValueAt(fila, 1)));     
             frm_SALA_OPERACION_TIPO_ANESTESIA.btnmodificar.setEnabled(true);
             frm_SALA_OPERACION_TIPO_ANESTESIA.btneliminar.setEnabled(true);
             String u=frm_SALA_OPERACION_TIPO_ANESTESIA.lblUsu.getText();
             frm_SALA_OPERACION_TIPO_ANESTESIA.lblUsu.setText(u);
             frm_SALA_OPERACION_TIPO_ANESTESIA.txtTipo_Anestesia.setEnabled(false);
       }
    }//GEN-LAST:event_tb_Tipo_AnestesiaKeyPressed

