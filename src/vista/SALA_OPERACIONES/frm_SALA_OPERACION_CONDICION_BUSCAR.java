    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        try{
            String condicion =txtBuscarCondicion.getText().toString();

            DefaultTableModel tabla= new DefaultTableModel();

            tabla.addColumn("Codigo");
            tabla.addColumn("Descripciòn");
            cst=con.prepareCall("{call SALA_OPERACION_CONDICION_BUSCAR(?)}");
            cst.setString(1, condicion);
            r=cst.executeQuery();
            while (r.next()){
                Object dato[]=new  Object[2];
                for (int i=0; i<2; i++){
                    dato[i]=r.getString(i+1);

                }
                tabla.addRow(dato);
            }

            this.tb_Condicion.setModel(tabla);

            formato();
            txtBuscarCondicion.setText("");
            tb_Condicion.getSelectionModel().setSelectionInterval(0, 0);
            tb_Condicion.requestFocus();
        }catch (Exception e){}

    }//GEN-LAST:event_btn_buscarActionPerformed

    private void tb_CondicionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_CondicionKeyPressed

        char teclaPresionada = evt.getKeyChar();

        if(teclaPresionada==KeyEvent.VK_ENTER &&
            this.tb_Condicion.getRowCount() == 0 &&
            this.tb_Condicion.getSelectedRow() == -1){

            JOptionPane.showMessageDialog(rootPane, "La tabla esta vacia");

        }else
        if(teclaPresionada==KeyEvent.VK_ENTER &&
            this.tb_Condicion.getRowCount() != 0 &&
            this.tb_Condicion.getSelectedRow() != -1){
            int fila = tb_Condicion.getSelectedRow();
            //frm_SALA_OPERACION_TIPO_ANESTESIA T = new frm_SALA_OPERACION_TIPO_ANESTESIA();
            //T.setVisible(true);
            dispose();

            frm_SALA_OPERACION_CONDICION.txtCodigo.setText(String.valueOf(tb_Condicion.getValueAt(fila, 0)));
            frm_SALA_OPERACION_CONDICION.txtCondicion.setText(String.valueOf(tb_Condicion.getValueAt(fila, 1)));
            frm_SALA_OPERACION_CONDICION.btnmodificar.setEnabled(true);
            frm_SALA_OPERACION_CONDICION.btneliminar.setEnabled(true);
            String u=frm_SALA_OPERACION_CONDICION.lblUsu.getText();
            frm_SALA_OPERACION_CONDICION.lblUsu.setText(u);
            frm_SALA_OPERACION_CONDICION.txtCondicion.setEnabled(false);
        }
    }//GEN-LAST:event_tb_CondicionKeyPressed

