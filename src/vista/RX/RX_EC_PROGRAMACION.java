    private void btnGuardarCabeceraResActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCabeceraResActionPerformed
      
        if(txtSIS.getText().equals("")||txtOTROS.getText().equals("")||fecha_inicio.equals("")){
            cargareliminar.setVisible(true);
            cargareliminar.setBackground(new Color(255,51,51)); 
            Mensaje.setText("Ocurrió un error verifique");
            eli.setVisible(false);
            noeli.setVisible(false);
        }else{
            if(tg==1){
                RX_EC_RESULTADO_CAB cn = new RX_EC_RESULTADO_CAB();
            String DIA= new SimpleDateFormat("dd").format(fecha_inicio.getDate());
            String MES= new SimpleDateFormat("MM").format(fecha_inicio.getDate());
            if(cn.CPT_verif(DIA,MES)>0){
                cargareliminar.setVisible(true);
                cargareliminar.setBackground(new Color(255,91,70)); 
                Mensaje.setText("Ya existe programación para el "+DIA+" del "+MES+ ", Verifique ");
                eli.setVisible(false);
                noeli.setVisible(false);
              
                }else{
                        RX_EC_RESULTADO_CAB rxg = new RX_EC_RESULTADO_CAB();
                        rxg.setSIS(Integer.parseInt(txtSIS.getText()));
                        rxg.setOTROS(Integer.parseInt(txtOTROS.getText()));
                        rxg.setFECHA(determinarFecha(fecha_inicio));
                        if(rxg.RX_CAJA_NUEVO()){
                            cargareliminar.setBackground(new Color(0,153,102));
                            Mensaje.setText("Registro Guardado");
                            eli.setText("SI");
                            noeli.setText("OK");
                            eli.setVisible(false);
                            noeli.setVisible(true);
                            cargareliminar.setVisible(true);
                            RX_EC_RESULTADO_CAB LIST = new RX_EC_RESULTADO_CAB();
                            String FECHA= new SimpleDateFormat("MM").format(fecha_inicio.getDate());
                            LIST.RX_CAJA_LISTA(FECHA,tb_examen_det);
                            txtSIS.setEditable(false);
                            txtOTROS.setEditable(false);
                            fecha_inicio.setEnabled(false);
                        }
                        else{
                            cargareliminar.setBackground(new Color(255,91,70));
                            Mensaje.setText("El registro ya existe Intente nuevamente");
                            eli.setText("SI");
                            noeli.setText("OK");
                            eli.setVisible(false);
                            noeli.setVisible(true);
                            cargareliminar.setVisible(true);
                        }
                }
            }else if(tg==2){
                cargareliminar.setVisible(true);
                cargareliminar.setBackground(new Color(255,153,51)); 
                Mensaje.setText("Desea Actualizar el Registro ?");
                eli.setVisible(true);
                noeli.setVisible(true);
            }
            
        }
    }//GEN-LAST:event_btnGuardarCabeceraResActionPerformed

