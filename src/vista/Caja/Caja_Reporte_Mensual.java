    public static int DIAS(int mes, int año){
        switch(mes){
            case 1: //EMERO
            case 3: //MARZO
            case 5: //MAYO
            case 7: //JULIO
            case 8: //AGOSTO
            case 10: //OCTUBRE
            case 12: //DICIEMBRE
                return 31;
            case 4: //ABRIL
            case 6: //JUNIO
            case 9: //SETIEMBRE
            case 11: //NOVIEMBRE
                return 30;
            case 2: //FEBREO
                if(((año%100==0)&&(año%400==0))||((año%100 !=0)&&(año%4 !=0)))
                    return 29;
                else
                    return 28;
            default:
                throw new java.lang.IllegalArgumentException("EL mes no es el correcto");
        }
    }

