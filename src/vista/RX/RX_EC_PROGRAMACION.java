    private void tb_examen_detMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_examen_detMouseClicked
        int fila=tb_examen_det.getSelectedRow();
        if(evt.getClickCount()==1){
            jLabel33.setText("Edición");
            lblID.setText(String.valueOf(tb_examen_det.getValueAt(fila, 3)));
            txtSIS.setText(String.valueOf(tb_examen_det.getValueAt(fila, 1)));
            txtOTROS.setText(String.valueOf(tb_examen_det.getValueAt(fila, 2)));
            String fechaSeleccionada1 = (String) tb_examen_det.getModel().getValueAt(fila, 4);
             try {
             DateFormat dfo = new SimpleDateFormat("dd/MM/yyyy");
             Date fecha = dfo.parse(fechaSeleccionada1);
             fecha_inicio.setDate(fecha);
            } catch (Exception e) {
            }
            btneditar.setEnabled(true);
            btneliminar.setEnabled(true);
            btnGuardarCabeceraRes.setEnabled(false);
        }

          
    }//GEN-LAST:event_tb_examen_detMouseClicked

