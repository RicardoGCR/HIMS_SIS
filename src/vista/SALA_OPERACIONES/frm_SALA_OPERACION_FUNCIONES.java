    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        ImageIcon i=new ImageIcon(this.getClass().getResource("/imagenes/iconos/guardar16x16.png"));
        SDO_FUNCIONES f=new SDO_FUNCIONES();
        SDO_FUNCIONES f1=new SDO_FUNCIONES();
        SDO_FUNCIONES f2=new SDO_FUNCIONES();
        try{
            if(txtGM.getText().equalsIgnoreCase("G")){
                if(txtFuncion.getText().equalsIgnoreCase("")){
                    JOptionPane.showMessageDialog(rootPane, "Verifique si ha ingresado todos los campos");
                }
                else if(f.SDO_Funciones_Ver(txtFuncion.getText())>0){
                    JOptionPane.showMessageDialog(rootPane, "El registro ya existe\nIntente nuevamente");
                    txtFuncion.setText("");
                    txtFuncion.requestFocus();
                }else{

                    int guardar = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea GUARDAR los datos?",
                        "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,i);
                    if(guardar == 0 ){
                        SDO_FUNCIONES fg = new SDO_FUNCIONES();
                        fg.setID_FUNCIONES(txtCodigo.getText());
                        fg.setDESCRIP_FUNCIONES(txtFuncion.getText());
                        fg.setNOM_USU(lblUsu.getText());

                        if(fg.SDO_Funciones_Guardar()){
                            JOptionPane.showMessageDialog(null, "Datos Guardados");
                            limpiar();
                            habilitar();
                            txtGM.setText("G");
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "El registro ya existe\nIntente nuevamente");

                        }}
                    }}else{
                        if(txtFuncion.getText().equalsIgnoreCase("")){
                            JOptionPane.showMessageDialog(rootPane, "Verifique si ha ingresado todos los campos");
                        }
                        else if(f1.SDO_Funciones_Codigo(txtFuncion.getText()).equalsIgnoreCase(txtCodigo.getText())==false && f2.SDO_Funciones_Ver(txtFuncion.getText())>0 ){
                            JOptionPane.showMessageDialog(rootPane, "El registro ya existe\nIntente nuevamente");
                            txtFuncion.setText("");
                            txtFuncion.requestFocus();
                        }else{
                            int modificar = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea MODIFICAR los datos?",
                                "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,i);
                            if(modificar == 0 ){
                                SDO_FUNCIONES fm= new SDO_FUNCIONES();
                                fm.setID_FUNCIONES(txtCodigo.getText());
                                fm.setDESCRIP_FUNCIONES(txtFuncion.getText());
                                fm.setNOM_USU(lblUsu.getText());

                                if(fm.SDO_Funciones_Modificar()){
                                    JOptionPane.showMessageDialog(this, "Datos Modificados");
                                    limpiar();
                                    habilitar();
                                    txtGM.setText("G");
                                }
                                else{
                                    JOptionPane.showMessageDialog(this, "El registro ya existe\nIntente nuevamente");

                                }}
                            }}}catch(Exception e) {
                                JOptionPane.showMessageDialog(this, "Ingrese todos los campos");

                            }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        ImageIcon ieli=new ImageIcon(this.getClass().getResource("/imagenes/iconos/eliminar16x16.png"));
        int eliminar = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea ELIMINAR?",
            "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,ieli );
        try{
            if(eliminar == 0 )
            {
                SDO_FUNCIONES el=new SDO_FUNCIONES();
                el.setID_FUNCIONES(txtCodigo.getText());
                if(el.SDO_Funciones_Eliminar())
                {
                    JOptionPane.showMessageDialog(this, "Datos Eliminados");
                    btnguardar.setEnabled(true);
                    btnmodificar.setEnabled(false);
                    btneliminar.setEnabled(false);
                    limpiar();
                }
            }
        }catch(Exception e){

        }
    }//GEN-LAST:event_btneliminarActionPerformed

