    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

         ImageIcon ieli=new ImageIcon(this.getClass().getResource("/imagenes/iconos/eliminar16x16.png")); 
        int eliminar = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea ELIMINAR?",
            "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,ieli );
        try{
            if(eliminar == 0 )
            {
                CTipoSustentacion obj = new CTipoSustentacion();
                obj.setCodigo(txtCodigoTipoSustentacion.getText());
                if(obj.eliminarTipoSustentacion()){
                    JOptionPane.showMessageDialog(this, "Datos Eliminados");
                    limpiar();
                    desabilitar();
                    btnGrabar.setVisible(true);
                    btnModificar.setVisible(true);
                    btnEliminar.setVisible(true);
                }
            }
            else{
                limpiar();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Selecione el Tipo de Sustentacion a eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

