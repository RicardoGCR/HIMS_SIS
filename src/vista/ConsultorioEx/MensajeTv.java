    private void tbMensajesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMensajesMouseClicked
        if(evt.getClickCount()==1){
            int fila = tbMensajes.getSelectedRow();
            txtIdTv.setText(String.valueOf(tbMensajes.getValueAt(fila, 0)));
            lblMant.setText("U");
        } else
        if(evt.getClickCount()==2){
            int fila = tbMensajes.getSelectedRow();
            ListaTv listaTv = new ListaTv();
            dispose();
            listaTv.setVisible(true);
            ListaTv.lblTexto.setText(String.valueOf(tbMensajes.getValueAt(fila, 1)));
        }
    }//GEN-LAST:event_tbMensajesMouseClicked

