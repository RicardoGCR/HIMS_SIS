    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtMensaje.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese un mensaje");
        } else {
        try {
            ConsultorioExtMensaje mensaje = new ConsultorioExtMensaje();
            if(lblMant.getText().equals("U"))
                mensaje.setId(Integer.parseInt(txtIdTv.getText()));
            mensaje.setMensaje(txtMensaje.getText());
            if(mensaje.mantenimientoConsultorioMensaje(lblMant.getText())==true){
                mensaje.listarMensaje(tbMensajes);
                txtMensaje.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Ocurri√≥ un error, verifique");
            }
        } catch (Exception e) {
            System.out.println("Error: btnGuardarActionPerformed" + e.getMessage());
        }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

