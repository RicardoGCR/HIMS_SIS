    public void cargarTipoAnestesia(){
       
    try{
       DefaultTableModel tabla= new DefaultTableModel();
      
       tabla.addColumn("Codigo");
       tabla.addColumn("Tipo de Anestesia");
 
       
       cst=con.prepareCall("{call SALA_OPERACION_TIPO_ANESTESIA_LISTAR}");
       r=cst.executeQuery();
       while (r.next()){
       Object dato[]=new  Object[2];
       for (int i=0; i<2; i++){
           dato[i]=r.getString(i+1);
       }
       tabla.addRow(dato);
       }
       this.tb_Tipo_Anestesia.setModel(tabla);
       formato();
       
       }catch (Exception e){
       }
     }
    
