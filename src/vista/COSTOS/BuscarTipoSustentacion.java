    private void tableTipoSustentacionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableTipoSustentacionKeyPressed
       char teclaPresionada = evt.getKeyChar();
       

       if(teclaPresionada==KeyEvent.VK_ENTER &&
               this.tableTipoSustentacion.getRowCount() == 0 && 
               this.tableTipoSustentacion.getSelectedRow() == -1){
                      
           JOptionPane.showMessageDialog(rootPane, "La tabla esta vacia");
           
       }else 
       if(teclaPresionada==KeyEvent.VK_ENTER &&
               this.tableTipoSustentacion.getRowCount() != 0 && 
               this.tableTipoSustentacion.getSelectedRow() != -1){
              int fila = tableTipoSustentacion.getSelectedRow();
              TipoSustentacion TS = new TipoSustentacion();
                TS.setVisible(true);
                dispose();

             TipoSustentacion.txtCodigoTipoSustentacion.setText(String.valueOf(tableTipoSustentacion.getValueAt(fila, 0)));
             TipoSustentacion.txtTipoSustentacion.setText(String.valueOf(tableTipoSustentacion.getValueAt(fila, 1)));     
             TipoSustentacion.btnModificar.setEnabled(true);
             TipoSustentacion.btnEliminar.setEnabled(true);
             String u=frmCostos.lblUsu.getText();
             TipoSustentacion.lblUsu.setText(u);
       }

        // TODO add your handling code here:
    }//GEN-LAST:event_tableTipoSustentacionKeyPressed

